/// 专栏数据模型
/// 用于存储付费专栏的基本信息
class ColumnItem {
  /// 专栏唯一标识ID
  final int id;

  /// 分类标签名称
  final String category;

  /// 分类标签背景色
  final Color catBg;

  /// 分类标签文字颜色
  final Color catColor;

  /// 专栏标题
  final String title;

  /// 福利描述
  final String benefits;

  /// 价格
  final String price;

  /// 图标（emoji表情）
  final String emoji;

  /// 预览内容列表
  final List<String> previewContent;

  /// 是否下架（下架状态显示灰色，用户无法点击进入）
  final bool isOffline;

  /// 是否已购买（运行时从数据库查询）
  /// 默认为false，不存储在静态数据中
  final bool isPurchased;

  const ColumnItem({
    required this.id,
    required this.category,
    required this.catBg,
    required this.catColor,
    required this.title,
    required this.benefits,
    required this.price,
    required this.emoji,
    required this.previewContent,
    this.isOffline = false,
    this.isPurchased = false,
  });

  /// 创建副本，可覆盖isPurchased字段
  ColumnItem copyWith({
    int? id,
    String? category,
    Color? catBg,
    Color? catColor,
    String? title,
    String? benefits,
    String? price,
    String? emoji,
    List<String>? previewContent,
    bool? isOffline,
    bool? isPurchased,
  }) {
    return ColumnItem(
      id: id ?? this.id,
      category: category ?? this.category,
      catBg: catBg ?? this.catBg,
      catColor: catColor ?? this.catColor,
      title: title ?? this.title,
      benefits: benefits ?? this.benefits,
      price: price ?? this.price,
      emoji: emoji ?? this.emoji,
      previewContent: previewContent ?? this.previewContent,
      isOffline: isOffline ?? this.isOffline,
      isPurchased: isPurchased ?? this.isPurchased,
    );
  }
}

