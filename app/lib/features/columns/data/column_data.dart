<h2>三、应届生五险一金避坑指南</h2>

<h3>3.1 试用期陷阱</h3>
<p><strong>误区一：试用期不交社保</strong></p>
<ul>
  <li><strong>真相：</strong>试用期内必须缴纳五险一金，这是法定要求</li>
  <li>试用期是劳动合同的组成部分，不是隔离期</li>
  <li>无论试用期长短，单位都应为职工缴纳各项社会保险费</li>
  <li>如单位拒绝，可向劳动监察部门投诉</li>
</ul>
<p><strong>误区二：转正后补缴</strong></p>
<ul>
  <li><strong>风险：</strong>补缴会影响医保报销、公积金贷款等权益</li>
  <li>医保断缴期间发生的医疗费用无法报销</li>
  <li>公积金断缴影响贷款额度计算</li>
  <li>要求单位在试用期内正常缴纳</li>
</ul>
<p><strong>应对策略：</strong></p>
<ul>
  <li>签订劳动合同时，明确约定"试用期缴纳五险一金"</li>
  <li>入职后第一个月就要查询缴纳情况</li>
  <li>保留好工资条和缴费记录作为证据</li>
</ul>

<h3>3.2 缴费基数陷阱</h3>
<p><strong>常见套路：</strong></p>
<ul>
  <li>按最低标准缴纳，而非实际工资</li>
  <li>基本工资按最低标准，其余作为"补贴"、"奖金"</li>
  <li>口头承诺按实际工资缴纳，实际按最低标准</li>
</ul>
<p><strong>影响分析：</strong></p>
<ul>
  <li>短期：到手工资看似增加，但长期福利受损</li>
  <li>养老保险：缴费基数低，退休后养老金少</li>
  <li>医疗保险：个人账户资金少，影响看病报销</li>
  <li>公积金：贷款额度降低，影响买房计划</li>
</ul>
<p><strong>维权要点：</strong></p>
<ul>
  <li>要求单位按实际工资缴纳</li>
  <li>查看工资条上的缴费基数</li>
  <li>可通过12333或社保官网查询缴费记录</li>
  <li>发现问题及时向社保部门举报</li>
</ul>

<h3>3.3 自愿放弃陷阱</h3>
<p><strong>单位套路：</strong></p>
<ul>
  <li>以现金补贴代替缴纳</li>
  <li>要求签署"自愿放弃社保"协议</li>
  <li>声称缴纳社保不划算，不如直接发钱</li>
</ul>
<p><strong>法律规定：</strong></p>
<ul>
  <li>缴纳五险一金是用人单位的法定义务，不能通过约定变更或放弃</li>
  <li>"自愿放弃"协议无效</li>
  <li>单位以现金补贴代替缴纳是违法行为</li>
</ul>
<p><strong>正确做法：</strong></p>
<ul>
  <li>拒绝签署任何放弃协议</li>
  <li>要求单位依法缴纳</li>
  <li>如单位坚持，可向劳动部门投诉</li>
  <li>保留好相关证据，必要时申请劳动仲裁</li>
</ul>

<h3>3.4 断缴影响分析</h3>
<p><strong>短期断缴（1-3个月）：</strong></p>
<ul>
  <li>养老保险：影响不大，可补缴</li>
  <li>医疗保险：断缴期间无法报销，个人账户余额可继续使用</li>
  <li>失业保险：缴费年限重新计算</li>
  <li>公积金：影响贷款申请</li>
</ul>
<p><strong>长期断缴（3个月以上）：</strong></p>
<ul>
  <li>影响购房资格（部分城市要求连续缴纳）</li>
  <li>影响落户资格（多数城市有社保要求）</li>
  <li>影响子女入学（部分城市需要父母社保）</li>
  <li>影响医保报销待遇</li>
</ul>
<p><strong>特殊情况处理：</strong></p>
<ul>
  <li>离职期间可选择灵活就业身份缴纳养老和医疗</li>
  <li>北京灵活就业每月约800-1200元</li>
